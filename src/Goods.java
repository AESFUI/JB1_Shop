public class Goods {
    private String name;
    private float weight;
    private Color color;
    private double price;
    private int quantity;

    public String getName() {
        return name;
    }

    public float getWeight() {
        return weight;
    }

    public Color getColor() {
        return color;
    }

    public double getPrice() {
        return price;
    }

    public int getQuantity() {
        return quantity;
    }
}
